#!/bin/bash

#  sh curl-scripts/auth_booking/booking_signin.sh 

EMAIL="a@a1.com"
PASSWORD="12"
API="http://localhost:4741"
URL_PATH="/sign-in"

curl "${API}${URL_PATH}" \
  --include \
  --request POST \
  --header "Content-Type: application/json" \
  --data '{
    "credentials": {
      "email": "'"${EMAIL}"'",
      "password": "'"${PASSWORD}"'"
    }
  }'

echo